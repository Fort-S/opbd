/*2.1.	Вывести информацию обо всех изданиях, стоимость которых меньше 50 рублей.*/
SELECT *
FROM [Издание]
WHERE [Цена] < 250

/*2.2.	Вывести информацию обо всех изданиях, стоимость которых больше 50, но менее 1000 рублей, используя логические операции.*/
SELECT *
FROM [Издание]
WHERE [Цена] > 250 AND [Цена] < 600

/*2.3.	Выполнить предыдущее задание, используя операцию BETWEEN.*/
SELECT *
FROM [Издание]
WHERE [Цена] BETWEEN 250 AND 600
