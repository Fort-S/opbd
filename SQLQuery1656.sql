/*5.1.	Вывести список названий организаций города в алфавитном порядке.*/
SELECT Название
FROM Организация
ORDER BY Название ASC

/*5.2.	Вывести информацию об изданиях, выполнив сортировку по убыванию стоимости за подписку.*/
SELECT *
FROM Издание
ORDER BY [Цена]

/*5.3.	Вывести информацию о трех изданиях с наибольшей стоимостью.*/
SELECT TOP 3 *
FROM Издание 
ORDER BY [Цена] DESC 
