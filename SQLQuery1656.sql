/*3.1.	Вывести количество изданий, выписываемых каждой из организаций города.*/
SELECT [Издание], COUNT(*) AS Количество_изданий
FROM [Подписка]
GROUP BY [Издание]



/*3.2.	Вывести минимальную стоимость для тех изданий, названия которых начинается на букву М.*/
SELECT MIN([Издание].Цена) AS МИНЦена,[Название]
FROM Издание
WHERE  [Название] LIKE 'И%'
GROUP BY [Название] 

/*4.	Изучение предложения HAVING.*/

/*4.1.	Вывести список дат, на которые приходится не менее трех подписок на издания.*/
SELECT [Подписка].[Дата]
FROM [Подписка]
GROUP BY [Дата]
HAVING count(*) = 1

/*4.2.	Выполнить предыдущий запрос для тех организаций города, номер которых больше 2.*/
SELECT [Подписка].[Дата]
FROM [Подписка]
WHERE [Организация] > 3000
GROUP BY [Дата]
HAVING count(*) = 1

